    /**
     * Retrieve an object according to the <code>themeForDirName</code>
     * fetch specification.
     *
     * @param context The editing context to use
     * @param dirNameBinding fetch spec parameter
     * @return the object retrieved, or null if one was not found
     */
    public static Theme themeForDirName(
            EOEditingContext context,
            String dirNameBinding
        )
    {
        EOFetchSpecification spec = WCFetchSpecification
            .fetchSpecificationNamed("themeForDirName", "Theme");

        NSMutableDictionary<String, Object> bindings =
            new NSMutableDictionary<String, Object>();

        if ( dirNameBinding != null )
        {
            bindings.setObjectForKey( dirNameBinding,
                                      "dirName" );
        }
        spec = spec.fetchSpecificationWithQualifierBindings( bindings );

        NSArray<Theme> objects =
            objectsWithFetchSpecification( context, spec );
        if (log.isDebugEnabled())
        {
            log.debug( "themeForDirName(ec"
                + ", " + dirNameBinding
                + "): " + objects );
        }

        if ( objects.count() == 0 )
        {
            return null;
        }
        else
        {
            return objects.objectAtIndex(0);
        }
    }


